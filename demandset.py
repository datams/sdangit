# converts params to d_list
def build_d_list(G,d_params):
	d_list=[]
	for d in d_params:
		# initialize demand
		temp_dem = dem.demand(G.nodes(),88,88)
		# set fields
		temp_dem.make_choice_concrete(d[0],d[1],d[2],d[3])
		# add to list
		d_list.append(temp_dem)
	return d_list

